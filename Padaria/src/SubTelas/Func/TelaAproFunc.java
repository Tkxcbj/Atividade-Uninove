    private void btnRecusarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRecusarActionPerformed
        int resp = JOptionPane.showConfirmDialog(null, "Deseja mesmo recusar o usuario?", "Confirmação", JOptionPane.YES_NO_OPTION);
        
        if(resp == JOptionPane.YES_NO_OPTION){
            
            PendentesDao dao = new PendentesDao();
            Login login = new Login();
            boolean status = dao.conectar();
            
            login.setUsuario(txtNome.getText());            
            
            if(status){
                resp = dao.apagar(login);
                
                if(resp == 1){
                    JOptionPane.showMessageDialog(null, "Usuario Recusado com sucesso");
                    atualizar();
                } else{
                    JOptionPane.showMessageDialog(null, "Não foi possivel recusar esse usuario");
                }
            }else{
                JOptionPane.showMessageDialog(null, "Não foi possivel conectar ao banco de dados");
            }
        }else{            
        }
    }//GEN-LAST:event_btnRecusarActionPerformed

    private void mostrar(String nome){
        PendentesDao dao = new PendentesDao();
        Pendentes pendentes = new Pendentes();
        
        boolean status = dao.conectar();
        if(status){
            pendentes = dao.verificar(nome);
            
            txtNome.setText(pendentes.getUsuario());
            pswSenha.setText(pendentes.getSenha());
        }else{
            JOptionPane.showMessageDialog(null, "Não foi possivel conectar ao banco de dados");
        } 
    }
    
    private void listar(){
        int pos = 0;
        DefaultTableModel tableModel = (DefaultTableModel) jtPendentes.getModel();
        PendentesDao dao = new PendentesDao();
        ArrayList<Pendentes> lista;
        
        boolean status = dao.conectar();
        if(status){
            lista = dao.pendentes();
            while(true){
                try{
                    tableModel.addRow(new Object[]{
                        lista.get(pos++)
                    });
                }catch(IndexOutOfBoundsException ex){
                    break;
                }
            }
            dao.desconectar();
        }else{
            JOptionPane.showMessageDialog(null, "Não foi possivel conectar ao banco de dados");
        }
    }
    
    private void atualizar(){
        int pos = 0;
        DefaultTableModel tableModel = (DefaultTableModel) jtPendentes.getModel();
        
        tableModel.removeRow(jtPendentes.getSelectedRow());
        pos++;
    }
    
