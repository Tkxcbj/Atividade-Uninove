    private void jtPendentesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtPendentesMouseClicked
        if(evt.getClickCount() == 2){
            if(jtPendentes.getSelectedRow() != -1){
                String funcionario = jtPendentes.getValueAt(jtPendentes.getSelectedRow(), 0).toString();
                mostrar(funcionario);
                jpTabela.setVisible(false);
                jpAprov.setVisible(true);
            }
        }
    }//GEN-LAST:event_jtPendentesMouseClicked

    private void btnAprovarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAprovarActionPerformed
        Login login = new Login();
        PendentesDao Pendentesdao = new PendentesDao();
        UsuarioDao Usuariodao = new UsuarioDao();
        
        login.setUsuario(txtNome.getText());
        login.setSenha(pswSenha.getText());
        if(cbxCargo.getSelectedItem().toString().equalsIgnoreCase("selecione")){
            JOptionPane.showMessageDialog(null, "SELECIONE não é um cargo valido");
            return;
        }else{
            login.setCargo(cbxCargo.getSelectedItem().toString());
        }
        
        boolean statusU = Usuariodao.conectar(), statusP = Pendentesdao.conectar();
        if(statusU){
            int resposta = Usuariodao.salvar(login), resp = Pendentesdao.apagar(login);
            if(resposta == 1){
                if(statusP){
                    if(resp == 1){
                        JOptionPane.showMessageDialog(null, "Usuario aprovado com sucesso");
                        atualizar();
                        jpAprov.setVisible(false);
                        jpTabela.setVisible(true);
                    }else{
                        JOptionPane.showMessageDialog(null, "Não foi possivel apagar o usuario do banco PENDENTES", "Erro", JOptionPane.ERROR);
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Não foi possivel conectar ao banco de dados (pendentes)");
                }
            }else if(resposta == 1062){
                JOptionPane.showMessageDialog(null, "O Usuario já está em uso");
            }else{
                JOptionPane.showMessageDialog(null, "Não foi salvar esse usuario");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Não foi possivel conectar ao banco de dados (usuario)");
        }
        Pendentesdao.desconectar();
        Usuariodao.desconectar();
    }//GEN-LAST:event_btnAprovarActionPerformed

